CREATE OR REPLACE FUNCTION FUN_GETBYID_CURSO(p_id INT)
RETURNS SETOF Curso
AS $$
BEGIN
	RETURN QUERY
	SELECT * FROM CURSO
	WHERE idcurso = p_id;
END;
$$LANGUAGE plpgsql;


